package com.cista.pidb.code.to;

public class FabCodeTo {
	private String fab;
	private String fabDescr;
	private String fabSap;
	private String vendorCode;
	public String getFab() {
		return fab;
	}
	public void setFab(String fab) {
		this.fab = fab;
	}
	public String getFabDescr() {
		return fabDescr;
	}
	public void setFabDescr(String fabDescr) {
		this.fabDescr = fabDescr;
	}
	public String getFabSap() {
		return fabSap;
	}
	public void setFabSap(String fabSap) {
		this.fabSap = fabSap;
	}
	public String getVendorCode() {
		return vendorCode;
	}
	public void setVendorCode(String vendorCode) {
		this.vendorCode = vendorCode;
	}
}
